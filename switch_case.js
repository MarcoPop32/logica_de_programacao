let dias = "sexta";

switch(dias){
    case "segunda":
    console.log("Começando a semana");
    break;

    case "terça":
    console.log("Mais um dia");
    break;

    case "quarta":
    console.log("Estamos na metade da semana uhu!");
    break;

    case "quinta":
    console.log("Estamos quase lá");
    break;

    case "sexta":
    console.log("Sextou");
    break;

    default:
        console.log("Final de semana chegou, uhu!")
}